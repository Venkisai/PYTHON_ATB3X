#for loops
#for repeating a function multiple times
#working mechanism of range is (start, stop, step)
for i in range(5):
    print(i)
for a in range(1,5):
    print(a)
for b in range(3,5):
     print(b)
#for even numbers from 1 to 100
for x in range(0,100,2):
    print(x)
#to print odd numbers from 1 to 100
for y in range(1,100,2):
    print(y)
#to print multiples of 3
for z in range(0,100,3):
    print(z)
#to print multiples of 5
for p in range(0,100,5):
    print(p)
for c in range(0,10,1):
    print(c)
